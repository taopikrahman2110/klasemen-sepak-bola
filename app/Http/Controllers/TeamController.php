    public function index()
    {
        $teams = Team::all();
        if ($teams->isEmpty()) {
            return redirect()->route('team.create')->with('warning', 'Belum ada data tim. Silakan tambahkan data terlebih dahulu.');
        }
        return view('team.index', compact('teams'));
    }

